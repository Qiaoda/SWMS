package cn.jitmarketing.hot.entity;

public class BigClass {

	
	public String scode;
	public String sname;
	
	public BigClass() {
	}
	
	public BigClass(String scode, String sname) {
		super();
		this.scode = scode;
		this.sname = sname;
	}

	public String getScode() {
		return scode;
	}

	public void setScode(String scode) {
		this.scode = scode;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	
	
}
